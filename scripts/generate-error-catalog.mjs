#!/usr/bin/env node
/**
 * FILE: scripts/generate-error-catalog.mjs
 * PURPOSE: Render `packages/server/supabase/functions/_shared/error-codes.ts`
 *          into `docs/ERROR_CATALOG.generated.md` and keep OpenAPI + the
 *          docs MDX page honest about the registry.
 *
 * Usage:
 *   node scripts/generate-error-catalog.mjs          # write generated md
 *   node scripts/generate-error-catalog.mjs --check  # fail on drift
 */

import { readFileSync, writeFileSync, existsSync } from 'node:fs'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const ROOT = resolve(__dirname, '..')
const REGISTRY = resolve(
  ROOT,
  'packages/server/supabase/functions/_shared/error-codes.ts',
)
const OPENAPI = resolve(
  ROOT,
  'packages/server/supabase/functions/api/routes/openapi.ts',
)
const OUT = resolve(ROOT, 'docs/ERROR_CATALOG.generated.md')
const DOCS_MDX = resolve(ROOT, 'apps/docs/content/concepts/error-catalog.mdx')

function extractCodes(src) {
  const block = src.match(/export const API_ERROR_CODES = \[([\s\S]*?)\] as const/)
  if (!block) throw new Error('API_ERROR_CODES array not found in error-codes.ts')
  const codes = []
  const re = /'([A-Z0-9_]+)'/g
  let m
  while ((m = re.exec(block[1]))) codes.push(m[1])
  return codes
}

function render(codes) {
  const lines = [
    '<!-- AUTO-GENERATED by scripts/generate-error-catalog.mjs — do not edit by hand. -->',
    '# API error catalog',
    '',
    'Source of truth: `packages/server/supabase/functions/_shared/error-codes.ts`.',
    '',
    'Every admin / SDK / webhook failure should return:',
    '',
    '```json',
    '{ "ok": false, "error": { "code": "<CODE>", "message": "<safe text>", "requestId": "<optional>" } }',
    '```',
    '',
    `## Codes (${codes.length})`,
    '',
    '| Code | Group |',
    '| --- | --- |',
  ]

  for (const code of codes) {
    lines.push(`| \`${code}\` | ${groupFor(code)} |`)
  }

  lines.push('')
  lines.push('## Groups')
  lines.push('')
  lines.push('- **Auth / identity** — missing or invalid JWT / API key / scope')
  lines.push('- **Tenant / scope** — org or project context missing or inaccessible')
  lines.push('- **Validation** — body / field / signature shape')
  lines.push('- **Persistence** — DB / RPC failures (message is always sanitized)')
  lines.push('- **Upstream** — GitHub, LLM, network, secret-scan')
  lines.push('- **Entitlement** — plan / quota gates')
  lines.push('- **Transport** — client abort / internal envelope')
  lines.push('')
  return `${lines.join('\n')}\n`
}

function groupFor(code) {
  if (
    /AUTH|TOKEN|API_KEY|SCOPE|UNAUTHORIZED|FORBIDDEN|SERVER_MISCONFIGURED|KEY_NOT|ORG_KEY/.test(
      code,
    )
  ) {
    return 'Auth / identity'
  }
  if (/ORG|PROJECT|ORGANIZATION/.test(code)) return 'Tenant / scope'
  if (
    /VALIDAT|BAD_|INVALID_|MISSING_BODY|MISSING_SIGNATURE|NO_SECRET|METHOD|NOT_FOUND|CONFLICT|DUPLICATE|RATE|EXPIRED/.test(
      code,
    )
  ) {
    return 'Validation'
  }
  if (/DB_|RPC_|INSERT_|UPDATE_|DELETE_/.test(code)) return 'Persistence'
  if (
    /NETWORK|TIMEOUT|UPSTREAM|GITHUB|GH_|LLM|KEYS_EXHAUSTED|SECRET_DETECTED/.test(code)
  ) {
    return 'Upstream'
  }
  if (/FEATURE_|PLAN_|QUOTA/.test(code)) return 'Entitlement'
  return 'Transport'
}

function assertOpenApiWired(openapiSrc) {
  const problems = []
  if (!openapiSrc.includes("from '../../_shared/error-codes.ts'")) {
    problems.push('openapi.ts must import API_ERROR_CODES from _shared/error-codes.ts')
  }
  if (!openapiSrc.includes('enum: [...API_ERROR_CODES]')) {
    problems.push("openapi.ts Error.code must use `enum: [...API_ERROR_CODES]`")
  }
  if (!openapiSrc.includes('requestId')) {
    problems.push('openapi.ts Error schema must document requestId')
  }
  return problems
}

function assertDocsPage(mdxSrc, codes) {
  const problems = []
  if (!mdxSrc.includes('ERROR_CATALOG.generated.md')) {
    problems.push('concepts/error-catalog.mdx must reference docs/ERROR_CATALOG.generated.md')
  }
  // Spot-check a few stable codes so the page cannot be an empty stub.
  for (const code of ['MISSING_AUTH', 'VALIDATION_ERROR', 'DB_ERROR', 'INTERNAL']) {
    if (!codes.includes(code)) continue
    if (!mdxSrc.includes(code)) {
      problems.push(`concepts/error-catalog.mdx should mention ${code}`)
    }
  }
  return problems
}

const check = process.argv.includes('--check')
const registrySrc = readFileSync(REGISTRY, 'utf8')
const codes = extractCodes(registrySrc)
const rendered = render(codes)

const openapiSrc = readFileSync(OPENAPI, 'utf8')
const openapiProblems = assertOpenApiWired(openapiSrc)

if (!existsSync(DOCS_MDX)) {
  console.error(`Missing docs page: ${DOCS_MDX}`)
  process.exit(1)
}
const mdxSrc = readFileSync(DOCS_MDX, 'utf8')
const docsProblems = assertDocsPage(mdxSrc, codes)

if (check) {
  const existing = existsSync(OUT) ? readFileSync(OUT, 'utf8') : ''
  const problems = [...openapiProblems, ...docsProblems]
  if (existing !== rendered) {
    problems.push(
      'docs/ERROR_CATALOG.generated.md is stale — run `node scripts/generate-error-catalog.mjs`',
    )
  }
  if (problems.length) {
    console.error('error-code catalog check FAILED')
    for (const p of problems) console.error(`  - ${p}`)
    process.exit(1)
  }
  console.log(`error-code catalog OK (${codes.length} codes)`)
  process.exit(0)
}

if (openapiProblems.length || docsProblems.length) {
  console.error('error-code catalog generate blocked by wiring issues:')
  for (const p of [...openapiProblems, ...docsProblems]) console.error(`  - ${p}`)
  process.exit(1)
}

writeFileSync(OUT, rendered)
console.log(`Wrote ${OUT} (${codes.length} codes)`)
