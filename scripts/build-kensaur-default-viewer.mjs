#!/usr/bin/env node
/**
 * Build scripts/cloudfront-kensaur-default-viewer.js by composing mushi apex
 * redirects + vendored glot.it SPA routing for the kensaur.us Default behavior.
 */

import { readFileSync, writeFileSync } from 'node:fs'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'

const ROOT = dirname(fileURLToPath(import.meta.url))

function renameHandler(source, name) {
  const withoutHeader = source.replace(/^\/\*\*[\s\S]*?\*\/\s*/, '')
  return withoutHeader.replace(/function handler\s*\(/, `function ${name}(`)
}

const mushi = readFileSync(join(ROOT, 'cloudfront-mushi-apex-redirect.js'), 'utf8')
const glot = readFileSync(join(ROOT, 'vendor/cloudfront-glot-spa-router.js'), 'utf8')

const combined = `/**
 * FILE: cloudfront-kensaur-default-viewer.js
 * PURPOSE: kensaur.us Default cache behavior — Mushi apex 301s first, then glot.it SPA routing.
 * GENERATED by scripts/build-kensaur-default-viewer.mjs — do not edit by hand.
 * RUNTIME: cloudfront-js-2.0
 */

${renameHandler(mushi, 'mushiApexHandler')}
${renameHandler(glot, 'glotSpaHandler')}

function handler(event) {
  var out = mushiApexHandler(event);
  if (out && out.statusCode) {
    return out;
  }
  if (out && out.method) {
    event.request = out;
  }
  return glotSpaHandler(event);
}
`

const outPath = join(ROOT, 'cloudfront-kensaur-default-viewer.js')
writeFileSync(outPath, combined)
console.log(`[build] wrote ${outPath}`)
