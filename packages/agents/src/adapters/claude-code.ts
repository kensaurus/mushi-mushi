import type { spawn as nodeSpawn } from 'node:child_process'
import type { FixAgent, FixContext, FixResult } from '../types.js'
import { checkFileScope, checkCircuitBreaker } from '../scope.js'

/** Handle to the Node `spawn` function. Declared as a type-only import so
 *  this module stays tree-shakeable for non-Node runtimes — the actual
 *  implementation is `await import`-ed lazily below. */
type SpawnFn = typeof nodeSpawn

/**
 * ClaudeCodeAgent
 * ==============================================================
 *
 * Wraps the local `claude` CLI (a.k.a. Claude Code) so the fix-dispatch
 * orchestrator can generate patches using the user's own entitled account
 * rather than raw Anthropic API credits.
 *
 * Gating
 * ------
 * This adapter is **disabled by default**. Opt in per-process with:
 *
 *     MUSHI_ENABLE_CLAUDE_CODE_AGENT=1
 *
 * Rationale: shelling out to a CLI inside a long-running worker is a
 * sensitive capability (spawns child processes, reads repo files, can write
 * to disk). We keep it behind an explicit flag so shared deployments never
 * invoke it unintentionally. When disabled, `generateFix` returns a
 * deterministic "not configured" result that the orchestrator records as a
 * failed attempt — matching the previous stubbed behaviour, so upgrading
 * the package is non-breaking.
 *
 * Transport
 * ---------
 * When enabled, the adapter spawns `claude --print --output-format json`
 * in the project checkout and pipes the fix prompt on stdin. This is the
 * supported non-interactive entry-point of Claude Code. We stream stdout,
 * parse the final JSON envelope, and extract the plan + diff.
 *
 * Override the binary via `MUSHI_CLAUDE_CODE_BIN` for tests or custom
 * installs (e.g. `MUSHI_CLAUDE_CODE_BIN=/opt/claude/bin/claude`).
 */
export class ClaudeCodeAgent implements FixAgent {
  name = 'claude_code'

  async generateFix(context: FixContext): Promise<FixResult> {
    const branch = `mushi/fix-${context.reportId.slice(0, 8)}`
    const enabled = process.env.MUSHI_ENABLE_CLAUDE_CODE_AGENT === '1'
      || process.env.MUSHI_ENABLE_CLAUDE_CODE_AGENT === 'true'

    // We reference this in the prompt below; suppressing unused in the
    // disabled path so lint stays clean when the flag is off.
    void context.reproductionSteps

    if (!enabled) {
      return {
        success: false,
        branch,
        filesChanged: [],
        linesChanged: 0,
        summary: 'Claude Code agent is disabled on this worker',
        error:
          'ClaudeCodeAgent is gated behind MUSHI_ENABLE_CLAUDE_CODE_AGENT=1. '
          + 'Enable the flag on the worker process (and ensure the `claude` CLI is '
          + 'installed and authenticated) to run real sessions.',
      }
    }

    // ----------------------------------------------------------------
    // Dynamic imports: Node-only. Keeps the module import-safe in
    // environments that never enable this flag (Deno edge, browser tests,
    // etc.). If `node:child_process` is unavailable we fall back to the
    // disabled result with a clear error message rather than crashing.
    // ----------------------------------------------------------------
    let spawn: SpawnFn
    try {
      ;({ spawn } = await import('node:child_process'))
    } catch (err) {
      return {
        success: false,
        branch,
        filesChanged: [],
        linesChanged: 0,
        summary: 'Claude Code agent requires a Node runtime',
        error: `node:child_process unavailable (${err instanceof Error ? err.message : String(err)})`,
      }
    }

    const bin = process.env.MUSHI_CLAUDE_CODE_BIN || 'claude'
    const prompt = this.#buildPrompt(context)

    try {
      const output = await this.#runCli(spawn, bin, prompt)
      const parsed = this.#parseEnvelope(output)

      const filesChanged = parsed.filesChanged ?? []
      const linesChanged = parsed.linesChanged ?? 0

      return {
        success: parsed.success,
        branch,
        filesChanged,
        linesChanged,
        summary: parsed.summary ?? (parsed.success ? 'Fix generated by Claude Code' : 'Claude Code returned no changes'),
        error: parsed.error,
      }
    } catch (err) {
      return {
        success: false,
        branch,
        filesChanged: [],
        linesChanged: 0,
        summary: 'Claude Code invocation failed',
        error: err instanceof Error ? err.message : String(err),
      }
    }
  }

  validateResult(context: FixContext, result: FixResult): { valid: boolean; errors: string[] } {
    const errors: string[] = []

    const lineCheck = checkCircuitBreaker(result.linesChanged, context.config.maxLines)
    if (!lineCheck.allowed) errors.push(lineCheck.reason!)

    for (const file of result.filesChanged) {
      const scopeCheck = checkFileScope(file, context.report.component, context.config.scopeRestriction)
      if (!scopeCheck.allowed) errors.push(scopeCheck.reason!)
    }

    return { valid: errors.length === 0, errors }
  }

  #buildPrompt(context: FixContext): string {
    const lines: string[] = []
    lines.push('You are an autonomous bug-fix agent. Produce a minimal, safe patch.')
    lines.push('')
    lines.push(`Report: ${context.report.description}`)
    lines.push(`Category: ${context.report.category}  |  Severity: ${context.report.severity}`)
    if (context.report.component) lines.push(`Component: ${context.report.component}`)
    if (context.report.rootCause) lines.push(`Root cause: ${context.report.rootCause}`)
    if (context.reproductionSteps.length > 0) {
      lines.push('')
      lines.push('Reproduction steps:')
      context.reproductionSteps.forEach((s, i) => lines.push(`${i + 1}. ${s}`))
    }
    lines.push('')
    lines.push(`Max lines of diff: ${context.config.maxLines}. Scope: ${context.config.scopeRestriction}.`)
    lines.push('')
    lines.push('Respond with a JSON object: {"success": bool, "summary": string, "filesChanged": string[], "linesChanged": number, "error"?: string}. No prose outside JSON.')
    return lines.join('\n')
  }

  #runCli(
    spawn: SpawnFn,
    bin: string,
    prompt: string,
  ): Promise<string> {
    return new Promise((resolve, reject) => {
      const child = spawn(bin, ['--print', '--output-format', 'json'], {
        stdio: ['pipe', 'pipe', 'pipe'],
      })
      let stdout = ''
      let stderr = ''
      const timeout = setTimeout(() => {
        child.kill('SIGTERM')
        reject(new Error('Claude Code CLI timed out after 5m'))
      }, 5 * 60_000)

      child.stdout.on('data', (chunk: Buffer) => { stdout += chunk.toString('utf8') })
      child.stderr.on('data', (chunk: Buffer) => { stderr += chunk.toString('utf8') })
      child.on('error', (err) => {
        clearTimeout(timeout)
        reject(err)
      })
      child.on('close', (code) => {
        clearTimeout(timeout)
        if (code !== 0) {
          reject(new Error(`claude CLI exited ${code}: ${stderr.trim().slice(0, 500)}`))
          return
        }
        resolve(stdout)
      })
      child.stdin.end(prompt)
    })
  }

  #parseEnvelope(output: string): {
    success: boolean
    summary?: string
    filesChanged?: string[]
    linesChanged?: number
    error?: string
  } {
    // Claude Code emits a JSON object per turn; the final line is the
    // completion envelope. Parse loosely — if the envelope format changes
    // we return success=false with the raw text rather than crashing.
    const trimmed = output.trim()
    if (!trimmed) return { success: false, error: 'Claude Code returned empty output' }

    // Try last-line JSON first (non-stream mode).
    const lines = trimmed.split(/\r?\n/).reverse()
    for (const line of lines) {
      const t = line.trim()
      if (!t.startsWith('{')) continue
      try {
        const parsed = JSON.parse(t) as Record<string, unknown>
        if (typeof parsed.success === 'boolean') {
          return {
            success: parsed.success,
            summary: typeof parsed.summary === 'string' ? parsed.summary : undefined,
            filesChanged: Array.isArray(parsed.filesChanged)
              ? (parsed.filesChanged.filter((x) => typeof x === 'string') as string[])
              : undefined,
            linesChanged: typeof parsed.linesChanged === 'number' ? parsed.linesChanged : undefined,
            error: typeof parsed.error === 'string' ? parsed.error : undefined,
          }
        }
      } catch {
        // keep looking
      }
    }
    return { success: false, error: `Could not parse Claude Code envelope: ${trimmed.slice(0, 200)}` }
  }
}
